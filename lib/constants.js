export const getProjectBySlug = (slug) => {
  return architects.find((project) => project.slug === slug);
};

export const getPrevsiousProject = (slug) => {
  const index = architects.findIndex((project) => project.slug === slug);
  if (index === 0) {
    return null;
  }
  return architects[index - 1];
};

export const getNextProject = (slug) => {
  const index = architects.findIndex((project) => project.slug === slug);
  if (index === architects.length - 1) {
    return null;
  }
  return architects[index + 1];
};
